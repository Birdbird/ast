		<h2 class="round">科协简介</h2>
		<p>&nbsp;&nbsp;&nbsp;&nbsp;海门市科学协会（简称海门市科协）是海门市科学技术工作者的群众组织，是中国共产党海门市委员领导下的人民团体是党和政府联系科技工作者的桥梁和纽带，是发展科学技术事业的重要社会力量。</br>
&nbsp;&nbsp;&nbsp;&nbsp;海门市科学技术1959年2月7日正式成立。第五次代表大会于2003年4月23日召开，主席仇洪泽、副主席黄永健、陈亚康、姜志斌、姜美平。</br>
&nbsp;&nbsp;&nbsp;&nbsp;海门市科学技术协会下设事业部、学会部、普及部。现有各类学会（协会）、研究会116个，镇科协23个，企业科协6个，共有会员1万多名。</br>
&nbsp;&nbsp;&nbsp;&nbsp;主要任务是：开展学术交流，活跃学术思想，促进学科发展，促进科技成果转化为生产力。普及科学知识，弘扬科学精神，传播科学思想和科学方法，推广先进技术，开展青少年科学技术教育，举办多种形式的大型科普宣传活动。开展科技咨询和决策咨询，接受委托承担项目评估、成果鉴定、专业技术职称评定等任务，搞好继续教育和技术培训工作。反映科学技术工作者的意见和要求，维护科学技术工作者的合法权益。宣传和表彰在科协工作和科技活动中取得优秀成绩的集体和个人，举荐人才。近年来，海门市科协在各学会、协会、企业科协，镇、街道科协的支持帮助下，紧紧围绕海门市经济建设和社会发展中心工作，开展各项活动，得到了社会各界的肯定，为海门市三个文明建设作出了贡献。</p>
